    private void btnDeleteEnterpriseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteEnterpriseActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblEnterprise.getSelectedRow();
        if (selectedRow >= 0) {
            int response = JOptionPane.showConfirmDialog(null, "Do you want to delete the Enterprise details?", "removeEnterprise", JOptionPane.YES_NO_OPTION);
            if (response == JOptionPane.YES_OPTION) {

                Network network = (Network) tblEnterprise.getValueAt(selectedRow, 1);
                EnterpriseType type = (EnterpriseType) tblEnterprise.getValueAt(selectedRow, 3);
                if (type.equals(EnterpriseType.Financial)) {
                    networkDir.getNetworkList().remove(network);
                    populateTable();
                    JOptionPane.showMessageDialog(null, "Enterprise deleted successfully.");
                } else {
                    JOptionPane.showMessageDialog(null, "This enterprise cannot be deleted.");
                }

            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select any row.");
        }
    }//GEN-LAST:event_btnDeleteEnterpriseActionPerformed

