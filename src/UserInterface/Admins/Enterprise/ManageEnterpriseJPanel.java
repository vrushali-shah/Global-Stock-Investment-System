    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblEnterprise.getModel();

        model.setRowCount(0);
        for (Network network : networkDir.getNetworkList()) {
            for (Enterprise e : network.getEnterpriseDirectory().getEnterpriseList()) {
                for (UserAccount ua : e.getUserDirectory().getUserAccountList()) {
                    Object[] row = new Object[6];
                    row[0] = network.getId();
                    row[1] = network;
                    row[2] = e.getName();
                    row[3] = e.getType();
                    row[4] = ua.getPerson().getFirstName() + " " + ua.getPerson().getLastName();
                    row[5] = ua.getUserName();
                    model.addRow(row);
                }

            }
        }
    }

