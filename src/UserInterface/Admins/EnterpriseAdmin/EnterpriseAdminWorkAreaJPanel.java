    public void populateOrgTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblOrg.getModel();
        dtm.setRowCount(0);
        for (Organisation o : enterprise.getOrganisationDirectory().getOrganisationList()) {
            Object row[] = new Object[2];
            row[0] = o;
            row[1] = o.getType();
            dtm.addRow(row);
        }
    }

