    private void btnViewOrgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewOrgActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblOrg.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select the row from table to view details", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            Organisation org = (Organisation) tblOrg.getValueAt(selectedRow, 0);
            Enterprise enterprise = org.getInEnterprise();
            ViewOrganisationJPanel viewOrganisationJPanel = new ViewOrganisationJPanel(userProcessContainer, account, org, enterprise);
            userProcessContainer.add("viewOrganisationJPanel", viewOrganisationJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnViewOrgActionPerformed

    private void btnManageEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnManageEmpActionPerformed
        // TODO add your handling code here:

        int selectedRow = tblOrg.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select the row from table to manage employee details", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            Organisation org = (Organisation) tblOrg.getValueAt(selectedRow, 0);
            ManageEmployeeJPanel manageEmployeeJPanel = new ManageEmployeeJPanel(userProcessContainer, account, org);
            userProcessContainer.add("ManageEmployeeJPanel", manageEmployeeJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnManageEmpActionPerformed

