    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblPerson.getModel();
        dtm.setRowCount(0);
        for (UserAccount ua : org.getUserDirectory().getUserAccountList()) {
           
            Object row[] = new Object[4];
            row[0] = ua.getPerson().getFirstName();
            row[1] = ua.getPerson().getLastName();
            row[2] = ua;
            row[3]=ua.isIsActive()?"Active":"Disabled";
            dtm.addRow(row);
        }
    }

