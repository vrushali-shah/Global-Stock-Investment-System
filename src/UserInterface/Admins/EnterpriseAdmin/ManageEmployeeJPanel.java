    private void btnViewEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewEmpActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblPerson.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select the row from table to view details", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            UserAccount user = (UserAccount) tblPerson.getValueAt(selectedRow, 2);
            ViewEmployeeJPanel viewEmployeeJPanel = new ViewEmployeeJPanel(userProcessContainer, account, user, org);
            userProcessContainer.add("ViewEmployeeJPanel", viewEmployeeJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnViewEmpActionPerformed

