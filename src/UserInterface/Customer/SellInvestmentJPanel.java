    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblSell.getModel();

        model.setRowCount(0);

        for (Map.Entry<Stock, StockDetails> entry : customer.getStockDetailMap().entrySet()) {

            Object[] row = new Object[7];
            Stock s = entry.getKey();
            row[0] = s.getStockId();
            row[1] = s;
            row[2] = s.getNetwork();
            double price = s.getStockHistoryList().get(0).getPrice();

            row[3] = price;
            row[4] = entry.getValue().getRunningTotal();
            row[5] = entry.getValue().getQuantity();
            row[6] = "0";
            model.addRow(row);
            
        }

    }

    private void populateNetworkCombo() {
        for (Network n : ecosystem.getNetworkDirectory().getNetworkList()) {
            networkCombo.addItem(n);
        }
    }

    private void populateEnterpriseCombo(Network n) {
        enterpriseCombo.removeAllItems();
        for (Enterprise e : n.getEnterpriseDirectory().getEnterpriseList()) {
            if (e instanceof FinancialEnterprise) {
                enterpriseCombo.addItem(e);
            }
        }

    }

