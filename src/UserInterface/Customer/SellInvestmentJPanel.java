    private void btnRequestInvestmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRequestInvestmentActionPerformed
        // TODO add your handling code here:
        if (areQuantityFine()) {
            Customer c = (Customer) account.getPerson();
            InvestmentWorkRequest iwr = new InvestmentWorkRequest(1);
            iwr.setStatus("Requested");
            Enterprise e = (Enterprise) enterpriseCombo.getSelectedItem();
            c.getWorkQueue().addWorkRequest(iwr);
            for (Organisation o : e.getOrganisationDirectory().getOrganisationList()) {
                if (o instanceof InvestmentAndRiskOrganization) {
                    o.getWorkQueue().getWorkRequestList().add(iwr);
                    iwr.setOrganisation(o);
                }
            }

            for (int i = 0; i < tblSell.getRowCount(); i++) {
                if (tblSell.getValueAt(i, 6) != null) {
                    Integer value=Integer.parseInt((String) tblSell.getValueAt(i, 6));
                  
                        if(value!=0){
                        InvestmentItem item = new InvestmentItem();
                        item.setStock((Stock) tblSell.getValueAt(i, 1));
                        item.setQuantity(value);
                        iwr.getInvestment().addInvestmentItem(item);
                    }
                }
            }
JOptionPane.showMessageDialog(null, "Request has been sent", "Warning", JOptionPane.WARNING_MESSAGE);
     
        } else {
            JOptionPane.showMessageDialog(null, "Cant sell more than you have", "Warning", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btnRequestInvestmentActionPerformed

    private boolean areQuantityFine() {
        for (int i = 0; i < tblSell.getRowCount(); i++) {
                if (String.valueOf(tblSell.getValueAt(i, 6)).trim().isEmpty()) {
                return false;
                }
                else{
                    int owned =Integer.parseInt((String) tblSell.getValueAt(i, 5));
                    int toSell = Integer.parseInt((String) tblSell.getValueAt(i, 6));
                    if (toSell > owned || toSell <0) {
                        return false;
                    }
                }
            
        }
        return true;
    }

