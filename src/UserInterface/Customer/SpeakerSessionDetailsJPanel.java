    private void btnRegCanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegCanActionPerformed
        // TODO add your handling code here:
        Customer c = (Customer) account.getPerson();
        if (isRegisterd) {
            session.getRegisteredCustomerList().remove(c);
            c.getRegisteredSessionsList().remove(session);
        } else {
            session.getRegisteredCustomerList().add(c);
            c.getRegisteredSessionsList().add(session);
        } 
        Component[] componentArray = speakerContainer.getComponents();
        Component component = componentArray[componentArray.length - 1];
        SpeakerSessionJPanel speakerSessionJPanel = (SpeakerSessionJPanel) component;
        speakerSessionJPanel.networkCombo.setSelectedIndex(0);
        populate();
    }//GEN-LAST:event_btnRegCanActionPerformed

