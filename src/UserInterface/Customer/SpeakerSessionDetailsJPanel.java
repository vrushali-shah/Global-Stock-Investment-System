    private void populate() {
        SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
        idTxt.setText(session.toString());
        dateTxt.setText(formatter.format(session.getDate()));
        startTimeTxt.setText(session.getStartTime().toString());
        endTimeTxt.setText(session.getEndTime().toString());
        totalSeatsTxt.setText(String.valueOf(session.getCapacity()));
        seatsAvailableTxt.setText(String.valueOf(session.getRemaining()));
        statusTxt.setText(session.getStatus());
        isRegisterd = session.getRegisteredCustomerList().contains((Customer) account.getPerson());

        if (isRegisterd) {
            regStatusTxt.setText("Yes");
            btnRegCan.setText("Cancel");
        } else {
            regStatusTxt.setText("No");
            btnRegCan.setText("Register");
        }
    }

