    private void btnCancelExpSessionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelExpSessionActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblSpeakerSessions.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a session first", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            String status=(String) tblSpeakerSessions.getValueAt(selectedRow, 6);
            if(status.equalsIgnoreCase("no")){
        JOptionPane.showMessageDialog(null, "You are not registered for this session", "Information", JOptionPane.INFORMATION_MESSAGE);
            }
            else{
            SpeakerSession session = (SpeakerSession) tblSpeakerSessions.getValueAt(selectedRow, 0);

            session.getRegisteredCustomerList().remove(c);
            c.getRegisteredSessionsList().remove(session);
            }
        }
    }//GEN-LAST:event_btnCancelExpSessionActionPerformed

    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblSpeakerSessions.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a session first", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            SpeakerSession session = (SpeakerSession) tblSpeakerSessions.getValueAt(selectedRow, 0);

            SpeakerSessionDetailsJPanel speakerSessionDetailsJPanel = new SpeakerSessionDetailsJPanel(speakerContainer, session, account);
            speakerContainer.add("SpeakerSessionDetailsJPanel", speakerSessionDetailsJPanel);
            CardLayout layout = (CardLayout) speakerContainer.getLayout();
            layout.next(speakerContainer);

        }
    }//GEN-LAST:event_btnRegisterActionPerformed

