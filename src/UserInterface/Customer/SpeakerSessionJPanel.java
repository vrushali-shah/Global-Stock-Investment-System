    public void populateTable(Network n) {
        DefaultTableModel model = (DefaultTableModel) tblSpeakerSessions.getModel();

        model.setRowCount(0);
        Customer c = (Customer) account.getPerson();
        SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
        for (SpeakerSession session : n.getSpeakerSessionList()) {
            Object[] row = new Object[7];

            row[0] = session;
            row[1] = formatter.format(session.getDate());
            row[2] = session.getStartTime();
            row[3] = session.getEndTime();
            row[4] = session.getSpeaker().getFirstName() + " " + session.getSpeaker().getLastName();
            row[5] = session.getStatus();
            row[6] = session.getRegisteredCustomerList().contains(c) ? "Yes" : "No";
            model.addRow(row);
        }

    }

    private void populateNetworkCombo() {
        for (Network n : ecosystem.getNetworkDirectory().getNetworkList()) {
            networkCombo.addItem(n);
        }
    }

