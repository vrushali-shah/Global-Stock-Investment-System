    private void populateNetworkCombo() {
        for (Network n : ecosystem.getNetworkDirectory().getNetworkList()) {
            networkCombo.addItem(n);
        }
    }

    private void populateEnterpriseCombo(Network n) {
        enterpriseCombo.removeAllItems();
        for (Enterprise e : n.getEnterpriseDirectory().getEnterpriseList()) {
            if (!(e instanceof GovernmentEnterprise)) {
                enterpriseCombo.addItem(e);
            }
        }

    }

    public void populateTable(Enterprise e) {
        DefaultTableModel model = (DefaultTableModel) tblRequestedInv.getModel();

        model.setRowCount(0);
        Customer c = (Customer) account.getPerson();
        SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
        for (WorkRequest wr : c.getWorkQueue().getWorkRequestList()) {
            try{
            if (wr.getOrganisation().getInEnterprise() == e) {
                Object[] row = new Object[9];
                if (wr instanceof InvestmentWorkRequest) {
                    InvestmentWorkRequest iwr = (InvestmentWorkRequest) wr;
                    row[0] = iwr;
                    row[1] = "InvestmentWorkRequest";
                    row[2] = "NA";
                } else if (wr instanceof AdviceWorkRequest) {
                    AdviceWorkRequest awr = (AdviceWorkRequest) wr;
                    row[0] = awr;
                    row[1] = "AdviceWorkRequest";
                    row[2] = awr.getRisk().name();
                }
                row[3] = wr.getOrganisation().getInEnterprise().getInNetwork();
                row[4] = wr.getOrganisation().getInEnterprise();
                row[5] = wr.getStatus();
                row[6] = formatter.format(wr.getRequestDate());
                row[7] = wr.getResolveDate() != null ? formatter.format(wr.getResolveDate()) : "";
                row[8] = wr.getReceiver() != null ? wr.getReceiver() : "";

                model.addRow(row);
            }
        }
          catch(Exception ex){
                ex.getMessage();
                }
        }
      

    }

