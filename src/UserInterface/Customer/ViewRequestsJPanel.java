    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblRequestedInv.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select the row from table to view details", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            WorkRequest wr = (WorkRequest) tblRequestedInv.getValueAt(selectedRow, 0);
            if (wr instanceof AdviceWorkRequest) {
                if (wr.getStatus().equalsIgnoreCase("Completed")) {
                    AdviceWorkRequest awr = (AdviceWorkRequest) wr;
                    ViewAdviceRequestJPanel viewAdviceRequestJPanel = new ViewAdviceRequestJPanel(viewRequestContainer, awr);
                    viewRequestContainer.add("ViewAdviceRequestJPanel", viewAdviceRequestJPanel);
                    CardLayout layout = (CardLayout) viewRequestContainer.getLayout();
                    layout.next(viewRequestContainer);
                } else {
                    JOptionPane.showMessageDialog(null, "Request not completed yet.", "Warning", JOptionPane.INFORMATION_MESSAGE);

                }
            } else {

                InvestmentWorkRequest iwr = (InvestmentWorkRequest) wr;
                ViewInvestmentRequestJPanel viewInvestmentRequestJPanel = new ViewInvestmentRequestJPanel(viewRequestContainer, iwr);
                viewRequestContainer.add("ViewInvestmentRequestJPanel", viewInvestmentRequestJPanel);
                CardLayout layout = (CardLayout) viewRequestContainer.getLayout();
                layout.next(viewRequestContainer);
            }
        }
    }//GEN-LAST:event_btnViewActionPerformed

