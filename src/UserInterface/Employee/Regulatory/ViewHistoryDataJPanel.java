    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblGovernmentRqst.getModel();

         Organisation found = null;
        for (Organisation o : enterprise.getOrganisationDirectory().getOrganisationList()) {
            if (o instanceof RegulatroyOrganization) {
                found = o;
                break;
            }
        }
        
        model.setRowCount(0);
        for (WorkRequest wr : found.getWorkQueue().getWorkRequestList()) {
            if (wr instanceof GovernmentWorkRequest) {

                GovernmentWorkRequest gwr = (GovernmentWorkRequest) wr;
                if (gwr.getEnterpriseInvestMap().get(enterprise) != null) {
                    Object[] row = new Object[5];
                    row[0] = gwr.getStatus();
                    row[1] = gwr;
                    row[2] = (Employee) gwr.getSender().getPerson();
                    row[3] = (Customer) gwr.getCustomer();
                    row[4] = new SimpleDateFormat("yyyy-MM-dd").format(gwr.getRequestDate());
                    model.addRow(row);
                }
            }
        }
    }

    private void btnViewDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDataActionPerformed

        int selectedRow = tblGovernmentRqst.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select the row from table to view details", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            GovernmentWorkRequest gwr = (GovernmentWorkRequest) tblGovernmentRqst.getValueAt(selectedRow, 1);
            ViewSentDataJPanel panel = new ViewSentDataJPanel(provideDataContainer, gwr, enterprise);
            provideDataContainer.add("ViewSentDataJPanel", panel);
            CardLayout cardLayout = (CardLayout) provideDataContainer.getLayout();
            cardLayout.next(provideDataContainer);

        }

    }//GEN-LAST:event_btnViewDataActionPerformed

